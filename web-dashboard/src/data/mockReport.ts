function bestStreak(days: Array<{ reviews: number }>) {
  let best = 0;
  let current = 0;
  for (const day of days) {
    if (day.reviews > 0) {
      current += 1;
      best = Math.max(best, current);
    } else {
      current = 0;
    }
  }
  return best;
}

